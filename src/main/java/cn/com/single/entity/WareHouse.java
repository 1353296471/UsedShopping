package cn.com.single.entity;

public class WareHouse {
	private int proId;
	private int sizeId;
	private int colorId;
	private int cataId;
	private int num;

	public int getProId() {
		return proId;
	}

	public void setProId(int proId) {
		this.proId = proId;
	}

	public int getSizeId() {
		return sizeId;
	}

	public void setSizeId(int sizeId) {
		this.sizeId = sizeId;
	}

	public int getColorId() {
		return colorId;
	}

	public void setColorId(int colorId) {
		this.colorId = colorId;
	}

	public int getCataId() {
		return cataId;
	}

	public void setCataId(int cataId) {
		this.cataId = cataId;
	}

	public int getNum() {
		return num;
	}

	public void setNum(int num) {
		this.num = num;
	}

}
